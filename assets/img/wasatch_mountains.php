<p>The Wasatch Mountain Range runs north and south, dividing the Great Basin Region to the east and the Rocky Mountains to the west. Several dramatic, glacially-carved peaks punctuate the landscape near the 11,000 feet range. The tallest peak, Mount Nebo, sits at the southern end of the range at 11,928 feet. Plenty of snowfall makes this range a prime destination for downhill skiers. Alta, Snowbird, Brighton, Solitude and Sundance Ski Resorts are all located here. The Wasatch Mountains also boast excellent rock climbing, hiking and mountain biking. </p>
